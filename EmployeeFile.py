class Employee:
    def __init__(self, ID, name, salary, address, department, email, phone):
        self.ID = ID
        self.name = name
        self.salary = salary
        self.address = address
        self.department = department
        self.email = email
        self.phone = phone

    def getID(self):
        return self.ID
    def getName(self):
        return self.Name
    def getSalary(self):
        return self.Salary
    def getAddress(self):
        return self.Address
    def getDepartment(self):
        return self.Department
    def getEmail(self):
        return self.Email
    def getPhone(self):
        return self.Phone
